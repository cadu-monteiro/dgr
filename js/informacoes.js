

//Apaga o texto a cada clique em outra imagem
function ocultarParagrafos() {
  const paragrafos = document.getElementsByTagName("p");
  for (let i = 0; i < paragrafos.length; i++) {
    paragrafos[i].style.display = "none";
  }
}

//texto REX
function exibirParagraforex() {
  ocultarParagrafos();
  const paragrafo1 = document.getElementById("informacao-rex");
  paragrafo1.style.display = "block";
}
